//16.1 HTTP 통신
//16.1.3 HTTP 요청 메시지
/*
HTTP 요청 메시지는 요청 행, 요청 헤더, 메시지 본문으로 구성
요청 메서드 : 송수신 방법을 뜻하며, GET, POST, PUT, DELETE가 있음
GET : 웹 서버에 페이지 요청
POST : 서버의 데이터 갱신, 데이터 양이 많을 때, 비밀번호 등 개인정보를 보낼 때

요청 헤더 : 메세지의 제어 정보, 메시지 본문에 저장한 데이터 정보 저장
*/

//16.1.4 HTTP 응답 메세지
/*
응답 메시지는 응답 행, 응답 헤더, 메시지 본문으로 구성

응답 행 : HTTP 요청 상태 확인
응답 헤더 : 메시지의 제어 정보, 본문에 저장한 데이터 정보
메시지 본문 : 받을 데이터가 담김
*/
